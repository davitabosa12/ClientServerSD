    private void btnChangeVolumeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeVolumeActionPerformed
        int newVolume = new Random().nextInt();
        Message m = new Message("Generic", "Message");
        try{
            IoTAction action = new IoTAction(IoTAction.SET_VOLUME, newVolume);
            outToDevice.writeObject(action);
            m = (Message)inFromDevice.readObject();
            outToDevice.flush();
            
            
        }catch (IOException | ClassNotFoundException e){
            Logger.getLogger(DeviceTester.class.getName()).log(Level.SEVERE, null, e);
        }
        System.out.println(m.getMessage());
    }//GEN-LAST:event_btnChangeVolumeActionPerformed

