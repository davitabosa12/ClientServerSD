    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Message m = new Message("Generic","Message");
        try {
            IoTAction action = new IoTAction(IoTAction.TOGGLE);
            outToDevice.writeObject(action);
            
            m = (Message)inFromDevice.readObject();
            
        } catch (IOException ex) {
            Logger.getLogger(DeviceTester.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DeviceTester.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println(m.getMessage());
        try {
            outToDevice.flush();
        } catch (IOException ex) {
            Logger.getLogger(DeviceTester.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

